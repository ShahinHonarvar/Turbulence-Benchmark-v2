def is_prime(n):
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True

def composite_nums_between_indices(nums):
    return set((num for i, num in enumerate(nums) if 21 <= i <= 97 and (not is_prime(num))))