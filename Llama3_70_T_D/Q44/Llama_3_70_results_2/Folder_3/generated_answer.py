def is_prime(n):
    if n <= 1:
        return False
    if n == 2:
        return True
    if n % 2 == 0:
        return False
    i = 3
    while i * i <= n:
        if n % i == 0:
            return False
        i += 2
    return True

def composite_nums_between_indices(lst):
    return set((i for i in lst[66:94] if not is_prime(i)))