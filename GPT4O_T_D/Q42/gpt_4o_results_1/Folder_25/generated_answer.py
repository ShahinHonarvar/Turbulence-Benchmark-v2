def prime_factors(numbers):

    def get_primes(n):
        factors = set()
        i = 2
        while i * i <= n:
            if n % i:
                i += 1
            else:
                n //= i
                factors.add(i)
        if n > 1:
            factors.add(n)
        return factors
    if len(numbers) > 90:
        return get_primes(numbers[90])
    return set()