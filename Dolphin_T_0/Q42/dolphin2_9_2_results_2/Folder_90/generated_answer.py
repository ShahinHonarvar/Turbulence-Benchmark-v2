def prime_factors(numbers):

    def is_prime(n):
        if n < 2:
            return False
        for i in range(2, int(n ** 0.5) + 1):
            if n % i == 0:
                return False
        return True
    return set((factor for factor in range(1, numbers[263] + 1) if is_prime(factor) and numbers[263] % factor == 0))