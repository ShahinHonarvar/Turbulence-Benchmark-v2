def composite_nums_between_indices(lst):

    def is_prime(n):
        if n == 1:
            return False
        for i in range(2, int(n ** 0.5) + 1):
            if n % i == 0:
                return False
        return True

    def is_composite(n):
        return not is_prime(n)
    return set((i for i in lst[22:89] if is_composite(i)))