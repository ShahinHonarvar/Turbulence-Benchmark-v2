def prime_factors(numbers):

    def factors_helper(n):
        factors = set()
        while n % 2 == 0:
            factors.add(2)
            n //= 2
        for i in range(3, int(n ** 0.5) + 1, 2):
            while n % i == 0:
                factors.add(i)
                n //= i
        if n > 2:
            factors.add(n)
        return factors
    if len(numbers) > 67:
        return factors_helper(numbers[67])
    raise ValueError('List too short.')