def composite_nums_between_indices(nums):

    def is_prime(n):
        if n <= 1:
            return False
        if n <= 3:
            return True
        if n % 2 == 0 or n % 3 == 0:
            return False
        i = 5
        while i * i <= n:
            if n % i == 0 or n % (i + 2) == 0:
                return False
            i += 6
        return True
    composites = {num for i, num in enumerate(nums[56:83], start=56) if not is_prime(num)}
    return composites