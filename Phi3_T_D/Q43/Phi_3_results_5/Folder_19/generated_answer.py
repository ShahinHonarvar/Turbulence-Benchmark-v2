from typing import List

def find_primes_between_indices(numbers: List[int]) -> List[int]:

    def is_prime(n: int) -> bool:
        if n <= 1:
            return False
        if n <= 3:
            return True
        if n % 2 == 0 or n % 3 == 0:
            return False
        i = 5
        while i * i <= n:
            if n % i == 0 or n % (i + 2) == 0:
                return False
            i += 6
        return True
    return sorted(filter(is_prime, numbers[21:98]))