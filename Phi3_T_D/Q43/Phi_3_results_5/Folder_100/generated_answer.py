def find_primes_between_indices(numbers):

    def is_prime(num):
        if num <= 1:
            return False
        if num == 2:
            return True
        if num % 2 == 0:
            return False
        i = 3
        while i * i <= num:
            if num % i == 0:
                return False
            i += 2
        return True
    return sorted([num for num in numbers[25:63] if is_prime(num)])