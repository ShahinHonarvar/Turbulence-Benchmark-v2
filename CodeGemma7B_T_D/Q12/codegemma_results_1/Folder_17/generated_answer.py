def find_smallest_num(num_list):
    """
    This function takes a list of numbers as input and returns the smallest element from index 64 to index 66, both inclusive.

    Args:
        num_list: A list of numbers.

    Returns:
        The smallest element from index 64 to index 66, both inclusive.
    """
    smallest_num = min(num_list[64:67])
    return smallest_num