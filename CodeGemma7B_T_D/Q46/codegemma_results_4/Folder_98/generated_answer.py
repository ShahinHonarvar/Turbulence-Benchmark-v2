def gcf_three_nums(nums):
    num_one = nums[8]
    num_two = nums[2]
    num_three = nums[1]
    common_factor = 0
    if num_one % common_factor == 0:
        common_factor = num_one
    if num_two % common_factor == 0:
        common_factor = num_two
    if num_three % common_factor == 0:
        common_factor = num_three
    return common_factor