def find_original_set(a, b, c, d, e, f, g, h):
    original_set = set()
    for i in a:
        original_set.add(i)
    for i in b:
        original_set.add(i)
    for i in c:
        original_set.add(i)
    for i in d:
        original_set.add(i)
    for i in e:
        original_set.add(i)
    for i in f:
        original_set.add(i)
    for i in g:
        original_set.add(i)
    for i in h:
        original_set.add(i)
    return original_set