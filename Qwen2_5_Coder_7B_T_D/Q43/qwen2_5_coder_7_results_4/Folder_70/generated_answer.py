def is_prime(n):
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True

def find_primes_between_indices(nums):
    primes = []
    for i in range(430, 806):
        if is_prime(nums[i]):
            primes.append(nums[i])
    return sorted(primes)