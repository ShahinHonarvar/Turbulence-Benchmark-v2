def prime_factors(numbers):

    def is_prime(n):
        if n <= 1:
            return False
        for i in range(2, int(n ** 0.5) + 1):
            if n % i == 0:
                return False
        return True
    return set((factor for factor in range(2, numbers[35] + 1) if numbers[35] % factor == 0 and is_prime(factor)))