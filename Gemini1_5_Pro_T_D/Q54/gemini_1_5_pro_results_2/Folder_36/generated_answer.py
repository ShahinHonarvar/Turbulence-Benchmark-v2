def is_right_truncatable_prime(num):
    if num <= 1:
        return False
    if num == 2:
        return True
    if num % 2 == 0:
        return False
    for i in range(3, int(num ** 0.5) + 1, 2):
        if num % i == 0:
            return False
    while num > 0:
        if num < 10 and num > 1:
            return True
        if num % 2 == 0 or num % 5 == 0:
            return False
        for i in range(3, int(num ** 0.5) + 1, 2):
            if num % i == 0:
                return False
        num = num // 10
    return True