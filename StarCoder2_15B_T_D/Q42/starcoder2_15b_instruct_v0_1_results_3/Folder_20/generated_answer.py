def prime_factors(nums):
    return set(prime_factors_helper(nums[53]))

def prime_factors_helper(n):
    i = 2
    factors = []
    while i * i <= n:
        if n % i:
            i += 1
        else:
            n //= i
            factors.append(i)
    if n > 1:
        factors.append(n)
    return factors